int main() {
    // 测试复合赋值运算符
    int a = 5;
    a += 3; // 应该等于 8
    a *= 2; // 应该等于 16
    a -= 4; // 应该等于 12
    a /= 2; // 应该等于 6
    a %= 4; // 应该等于 2
    
    // 测试自增自减运算符
    int b = 10;
    b++; // 应该等于 11
    b--; // 应该等于 10
    
    // 测试逻辑运算符
    int c = 1;
    int d = 0;
    int e = c && d; // 应该等于 0
    int f = c || d; // 应该等于 1
    int g = !d; // 应该等于 1
    
    // 测试 for 循环外部初始化
    int sum = 0;
    int i = 0;
    for (; i < 5; i++) {
        sum += i;
    }
    
    // 返回所有测试结果的总和
    return a + b + e + f + g + sum;
}